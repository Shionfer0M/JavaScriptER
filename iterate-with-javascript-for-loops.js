/* for (a; b; c), donde aes la declaración de inicialización, bes la declaración de condición y ces la expresión final.

La instrucción de inicialización se ejecuta una sola vez antes de que comience el ciclo. Por lo general, se usa para definir y configurar su variable de ciclo.

La declaración de condición se evalúa al comienzo de cada iteración del ciclo y continuará mientras se evalúe true. Cuando la condición está falseal comienzo de la iteración, el ciclo dejará de ejecutarse. Esto significa que si la condición comienza como falsa, su ciclo nunca se ejecutará.

La expresión final se ejecuta al final de cada iteración de bucle, antes de la siguiente verificación de condición y generalmente se usa para incrementar o disminuir su contador de bucle. */